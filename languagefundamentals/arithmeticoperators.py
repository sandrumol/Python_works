num1=10
num2=3
add_res=num1+num2
print(f"addition result num1+num2={add_res}")

sub_res=num1-num2
print(f"subtraction result num1-num2={sub_res}")

mul_res=num1*num2
print(f"multiplication result num1*num2={mul_res}")

div_res=num1/num2
print(f"Division result num1/num2={div_res}")

exp_res=num1**num2
print(f"exponential result num1**num2={exp_res}")

floordiv_res=num1//num2
print(f"floor division result num1//num2={floordiv_res}")

modulo_res=num1%num2
print(f"modulo result num1%num2={modulo_res}")